    public ListaDeAlumnos() {
        conexion.conectar();
        initComponents();
        ResultSet alumnos = consultas.consulta("select * from persona join alumno on persona.cve_per=alumno.cve_per join statusalu on alumno.numControl_alu=statusalu.numControl_alu", conexion);
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre");
        modelo.addColumn("Numero de control");
        modelo.addColumn("Telefono");
        modelo.addColumn("Correo");
        modelo.addColumn("Estatus");
        tabla.setModel(modelo);
        String registros[] = new String[5];
        try {
            while (alumnos.next()) {                
                registros[0]=alumnos.getString("nom_per");
                registros[1]=alumnos.getString("numControl_alu");
                registros[2]=alumnos.getString("tel_per");
                registros[3]=alumnos.getString("mail_per");
                registros[4]=alumnos.getString("status_stalu");
                modelo.addRow(registros);
            }
        } catch (Exception e) {
        }
    }

